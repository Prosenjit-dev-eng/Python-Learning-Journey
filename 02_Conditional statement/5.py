# Problem: Suggest an activity based on the weather (e.g., Sunny - Go for a walk, Rainy - Read a book, Snowy - Build a snowman).
weather = input("Enter weather: ")

if (weather == "Sunny"):
        print("Go for a walk")
elif (weather == "Rainy"):
        print("Read a book")
elif (weather == "Snowy"):
        print("Biuld a snowman")
else: 
        print("Invalid weather")
        